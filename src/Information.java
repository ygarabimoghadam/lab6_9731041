public class Information {

    private Level l;
    private  String drug;
    private String type ;
    private String description ;
    private String numOfTheSickness;

    public Information() {
    }

    public Level getL() {
        return l;
    }

    public void setL(Level l) {
        this.l = l;
    }

    public String getDrug() {
        return drug;
    }

    public void setDrug(String drug) {
        this.drug = drug;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }


    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNumOfTheSickness() {

        return numOfTheSickness;
    }

    public void setNumOfTheSickness(String numOfTheSickness) {
        this.numOfTheSickness = numOfTheSickness;
    }


}


